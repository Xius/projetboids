Boids::Boids(void)
{
	flock = NULL;
	nba = 0;
}
Boids::Boids(int size)
{
	int i = 0;
	flock = new Agent[size];
	for(i=0; i<nba; i++)
	{
		flock[i]= Agent();
	}

}
// ===========================================================================
//                                  Destructor
// ===========================================================================
Boids::~Boids(void)
{
	delete flock;
}

